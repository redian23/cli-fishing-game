}

result(){
    if (( RANDOM % 2 )); 
        then got_off;
        else caught; 
    fi
}

bite(){
stty -echo
PROGRESS=0
bar=''

    BITE_ANIMATION        
    press_F

while true;
do
stty -echo
read -rs -N 1 -t 1 input

    if [ "$input" = "f" ] || [ "$input" = "а" ]; then     # Russian "a" on key button F 
        ((PROGRESS+=1))
        sleep 0.05
        bar="${bar} "
        
        echo -ne "\r"
        echo -ne "\e[46m$bar\e[0m"
        echo -n "${PROGRESS}%"
    else
        ((PROGRESS-=1))
        sleep 0.05
        bar="${bar} "
        
        echo -ne "\r"
        echo -ne "\e[41m$bar\e[0m"
        echo -n "${PROGRESS}%"
    fi

    if [ $PROGRESS = 100 ];then
        echo ""
        echo "What i'd catch ???"
        sleep 3s
        result
        break;
    fi
    
    if [ $PROGRESS = -10 ];then
        echo ""
        echo "Your fish-rod is broken (T_T)"
        sleep 3s
        break;
    fi
done
echo "" 
}

#Notification Restart or Exit 
quit(){
stty echo
clear 
echo "Restart or Exit "
echo 'Please enter your choice'
select opt in restart quit ; 
do
     case $opt in
       "restart")
            echo "you chose restart" 
            game
            ;;
        "quit")
            clear
            echo "Exit..."
            exit 0 
            ;;
        *) echo invalid option ;;
    esac
done
sleep 5s
}

