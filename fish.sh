}

caught(){
    gen_weigth
    clear
    echo "We are caught! :)"
    echo -e $(shuf -n1 -e "\e[31mCarpe\e[0m" "\e[32mRuff\e[0m" "\e[33mRoach\e[0m" "\e[34mPike\e[0m")
    echo "WEIGHT: $WEIGHT g."
